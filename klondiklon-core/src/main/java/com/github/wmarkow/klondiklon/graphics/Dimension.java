package com.github.wmarkow.klondiklon.graphics;

public class Dimension
{
    private float width;
    private float height;

    public Dimension(float width, float height) {
        this.width = width;
        this.height = height;
    }

    public float getWidth()
    {
        return width;
    }

    public float getHeight()
    {
        return height;
    }
}
